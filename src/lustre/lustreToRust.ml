  let append lhs rhs =
    I.fold (
      fun index svar trie -> match index with
      | _ :: index ->
        I.add (
          I.ListIndex (next_index_of trie) :: index
        ) svar trie
      | [] -> failwith "empty index of outputs"
    ) rhs lhs
