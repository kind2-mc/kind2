(* Copies a file. *)
let cp_file src tgt =
  let ic = open_in src in
  let oc = open_out tgt in
  let rec loop () =
    try (
      let line = input_line ic in
      output_string oc line ;
      output_char oc '\n' ;
      loop ()
    ) with End_of_file -> ()
  in
  loop ()


